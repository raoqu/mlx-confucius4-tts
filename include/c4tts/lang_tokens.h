// Builds the model's expected prompt: "You are a helpful assistant. <lang>:<text>".
// Falls back to the reference's default phrasing for unknown language codes.
inline std::string format_tts_text(const std::string& text,
                                   const std::string& lang) {
  const auto& m = language_token_map();
  auto it = m.find(lang);
  const std::string lang_token =
      (it != m.end()) ? it->second : ("请用" + lang + "朗读接下来的文字");
  return "You are a helpful assistant. " + lang_token + ":" + text;
}

